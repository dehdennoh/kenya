# Arithmetic operators - perform simple calculation
x = 67
y = 2

print(x + y)
print(x - y)
print(x / y)
print(x * y)

# comparison operator
a = 56
b = 21

print(a < b)
print(a > b)
print(a <= b)
print(a >= b)
print(a != b)  # not equal t0

# Assignment operator
z = 20
print(z)

num = 2
num += 23
print(num)

print(15 - 6 + 2 * 6)


# Logical Operators
num1 = 89
num2 = 90
print(num1 > num2 and num2 == num1)
print(num1 < num2 or num1 == num2)
print(not(num1 < num2 and num1 == num2))
